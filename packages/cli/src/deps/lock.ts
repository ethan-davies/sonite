import { existsSync, readFileSync, writeFileSync } from "node:fs";
import { join } from "node:path";
import { parse as parseToml } from "smol-toml";
import { getRegistryUrl } from "../config.js";
import { ProjectError } from "../project.js";

export interface LockPackage {
  readonly name: string;
  readonly version: string;
  /** SHA-256 hex digest of the downloaded `.tar.gz`. */
  readonly checksum: string;
  /** Registry base URL this package was resolved from. */
  readonly source: string;
  /** Direct dependency package names in the resolved graph. */
  readonly dependencies: readonly string[];
}

export interface Lockfile {
  readonly packages: readonly LockPackage[];
}

const LOCKFILE_HEADER = `\
# This file is automatically @generated by sn.
# It is not intended for manual editing.
`;

export function lockfilePath(projectRoot: string): string {
  return join(projectRoot, "project.lock");
}

/** Migrate legacy `sn.lock` → `project.lock` once if needed. */
export function migrateLegacyLockfile(projectRoot: string): void {
  const legacy = join(projectRoot, "sn.lock");
  const next = lockfilePath(projectRoot);
  if (!existsSync(next) && existsSync(legacy)) {
    const text = readFileSync(legacy, "utf8");
    writeFileSync(next, text, "utf8");
  }
}

export function loadLockfile(projectRoot: string): Lockfile | null {
  migrateLegacyLockfile(projectRoot);
  const path = lockfilePath(projectRoot);
  if (!existsSync(path)) {
    return null;
  }
  try {
    const raw = parseToml(readFileSync(path, "utf8")) as Record<
      string,
      unknown
    >;
    const list = raw.package;
    if (list === undefined) {
      return { packages: [] };
    }
    const entries = Array.isArray(list) ? list : [list];
    const packages: LockPackage[] = [];
    for (const entry of entries) {
      if (typeof entry !== "object" || entry === null) {
        throw new ProjectError("project.lock: invalid [[package]] entry");
      }
      const row = entry as Record<string, unknown>;
      if (
        typeof row.name !== "string" ||
        typeof row.version !== "string" ||
        typeof row.checksum !== "string"
      ) {
        throw new ProjectError(
          "project.lock: each package needs name, version, checksum strings",
        );
      }
      if (!row.name.trim() || !row.version.trim() || !row.checksum.trim()) {
        throw new ProjectError(
          "project.lock: name, version, and checksum must be non-empty",
        );
      }
      let source: string;
      if (row.source === undefined) {
        // Older lockfiles omit source; default to the active registry URL.
        source = getRegistryUrl();
      } else if (typeof row.source !== "string" || !row.source.trim()) {
        throw new ProjectError(
          "project.lock: source must be a non-empty string when present",
        );
      } else {
        source = row.source.trim().replace(/\/$/, "");
      }
      let dependencies: string[] = [];
      if (row.dependencies !== undefined) {
        if (
          !Array.isArray(row.dependencies) ||
          !row.dependencies.every((d) => typeof d === "string")
        ) {
          throw new ProjectError(
            "project.lock: dependencies must be an array of strings",
          );
        }
        dependencies = [...row.dependencies].sort();
      }
      packages.push({
        name: row.name,
        version: row.version,
        checksum: row.checksum,
        source,
        dependencies,
      });
    }
    return { packages };
  } catch (error) {
    if (error instanceof ProjectError) {
      throw error;
    }
    const message = error instanceof Error ? error.message : String(error);
    throw new ProjectError(`failed to parse project.lock: ${message}`);
  }
}

export function writeLockfile(
  projectRoot: string,
  packages: readonly LockPackage[],
): void {
  const sorted = [...packages].sort((a, b) => a.name.localeCompare(b.name));
  for (const pkg of sorted) {
    if (!pkg.checksum.trim()) {
      throw new ProjectError(
        `project.lock: refusing to write empty checksum for ${pkg.name}@${pkg.version}`,
      );
    }
  }

  const parts: string[] = [LOCKFILE_HEADER];
  if (sorted.length === 0) {
    parts.push("");
  } else {
    for (const pkg of sorted) {
      const source = (pkg.source.trim() || getRegistryUrl()).replace(/\/$/, "");
      parts.push("[[package]]");
      parts.push(`name = ${JSON.stringify(pkg.name)}`);
      parts.push(`version = ${JSON.stringify(pkg.version)}`);
      parts.push(`checksum = ${JSON.stringify(pkg.checksum)}`);
      parts.push(`source = ${JSON.stringify(source)}`);
      const deps = [...pkg.dependencies].sort();
      parts.push(
        `dependencies = [${deps.map((d) => JSON.stringify(d)).join(", ")}]`,
      );
      parts.push("");
    }
  }

  writeFileSync(lockfilePath(projectRoot), parts.join("\n"), "utf8");
}

export function ensureLockfile(projectRoot: string): void {
  migrateLegacyLockfile(projectRoot);
  if (!existsSync(lockfilePath(projectRoot))) {
    writeLockfile(projectRoot, []);
  }
}

export function lockPackageMap(
  lock: Lockfile | null,
): Map<string, LockPackage> {
  const map = new Map<string, LockPackage>();
  if (!lock) {
    return map;
  }
  for (const pkg of lock.packages) {
    map.set(pkg.name, pkg);
  }
  return map;
}
